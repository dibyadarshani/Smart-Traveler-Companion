package com.example.smarttravelercompanion;

public class CTrip {
    String email;
    String spoint;
    String dpoint;
    String tnotes;
    String tname;
    String tdate;
    String ttime;

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getSpoint() {
        return spoint;
    }

    public void setSpoint(String spoint) {
        this.spoint = spoint;
    }
    public String getDpoint() {
        return dpoint;
    }

    public void setDpoint(String dpoint) {
        this.dpoint = dpoint;
    }
    public String getTname() {
        return tname;
    }

    public void setTname(String tname) {
        this.tname = tname;
    }
    public String getTdate() {
        return tdate;
    }

    public void setTdate(String tdate) {
        this.tdate = tdate;
    }
    public String getTtime() {
        return ttime;
    }

    public void setTtime(String ttime) {
        this.ttime = ttime;
    }
    public String getTnotes() {
        return tnotes;
    }

    public void setTnotes(String tnotes) {
        this.tnotes = tnotes;
    }
}
